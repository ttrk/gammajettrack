float get_purity(int sys, int sample, int centmin, int centmax, int phoetmin, int phoetmax = 9999) {
    int cent_index = -1;
    if (centmin == 0 && centmax == 200)
        cent_index = 0;
    else if (centmin == 0 && centmax == 60)
        cent_index = 1;
    else if (centmin == 60 && centmax == 200)
        cent_index = 2;
    else if (centmin == 0 && centmax == 20)
        cent_index = 3;
    else if (centmin == 20 && centmax == 60)
        cent_index = 4;
    else if (centmin == 60 && centmax == 100)
        cent_index = 5;
    else if (centmin == 100 && centmax == 200)
        cent_index = 6;

    int pt_index = -1;
    if (phoetmin == 40 && phoetmax == 9999)
        pt_index = 0;
    else if (phoetmin == 60 && phoetmax == 9999)
        pt_index = 1;
    else if (phoetmin == 40 && phoetmax == 50)
        pt_index = 2;
    else if (phoetmin == 50 && phoetmax == 60)
        pt_index = 3;
    else if (phoetmin == 60 && phoetmax == 80)
        pt_index = 4;
    else if (phoetmin == 80 && phoetmax == 9999)
        pt_index = 5;
    else if (phoetmin == 80 && phoetmax == 100)
        pt_index = 6;
    else if (phoetmin == 100 && phoetmax == 9999)
        pt_index = 7;

    if (cent_index < 0 || pt_index < 0 || abs(sys) > 1 || sample < 0 || sample > 3)
        return -1;
    else
        return purity[sys][sample][pt_index * 7 + cent_index];
}

